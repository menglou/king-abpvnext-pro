using System.Text.Json.Serialization;

namespace King.AbpVnextPro.Paylink.Alipy.Domain
{
    /// <summary>
    /// DepartmentLabel Data Structure.
    /// </summary>
    public class DepartmentLabel : AlipayObject
    {
